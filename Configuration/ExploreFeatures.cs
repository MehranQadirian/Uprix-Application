        /// <summary>
        /// Get features filtered by category
        /// </summary>
        public static List<ExploreFeature> GetFeaturesByCategory(string category)
        {
            return GetAllFeatures()
                .Where(f => f.Category == category && f.IsEnabled)
                .OrderBy(f => f.Order)
                .ToList();
        }

        /// <summary>
        /// Get all enabled features ordered
        /// </summary>
        public static List<ExploreFeature> GetEnabledFeatures()
        {
            return GetAllFeatures()
                .Where(f => f.IsEnabled)
                .OrderBy(f => f.Order)
                .ToList();
        }

        /// <summary>
        /// Get all available categories
        /// </summary>
        public static List<string> GetCategories()
        {
            return GetAllFeatures()
                .Where(f => f.IsEnabled)
                .Select(f => f.Category)
                .Distinct()
                .OrderBy(c => c)
                .ToList();
        }
